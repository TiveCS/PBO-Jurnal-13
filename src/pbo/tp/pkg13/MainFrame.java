    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            Connection connection = SQLConnector.getInstance().getConnection();

            PreparedStatement s = connection.prepareStatement("SELECT * FROM pegawai;");
            ResultSet rs = s.executeQuery();

            while (rs.next()){
                Pegawai pegawai = new Pegawai();
                pegawai.setId(rs.getInt(1));
                pegawai.setName(rs.getString(2));
                pegawai.setPosisi(rs.getString(3));
                pegawai.setTglLahir(rs.getDate(4));

                DefaultListModel<Pegawai> model = (DefaultListModel<Pegawai>) listPegawai.getModel();
                model.addElement(pegawai);
            }

            connection.close();
        } catch (ClassNotFoundException | SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

    private void listPegawaiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listPegawaiValueChanged
        // TODO add your handling code here:
        Pegawai pegawai = listPegawai.getSelectedValue();
        if (pegawai == null){
            labelNama.setText("Nama: ");
            labelPosisi.setText("Posisi: ");
            labelLahir.setText("Tgl Lahir: ");
            
            btnHapus.setEnabled(false);
            btnUbah.setEnabled(false);
            return;
        }

       labelNama.setText("Nama: " + pegawai.getName());
       labelPosisi.setText("Posisi: " + pegawai.getPosisi());
       labelLahir.setText("Tgl Lahir: " + pegawai.getTglLahir());

       btnHapus.setEnabled(true);
       btnUbah.setEnabled(true);
    }//GEN-LAST:event_listPegawaiValueChanged

