    private void btnSaveTambahPegawaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveTambahPegawaiActionPerformed
        // TODO add your handling code here:
        String nama = textFieldNameTambahPegawai.getText();
        String posisi = null;
        if (radioGeneralTambahPegawai.isSelected()){
            posisi = "General";
        }else if (radioSergeantTambahPegawai.isSelected()){
            posisi = "Sergeant";
        }else if (radioCorporalTambahPegawai.isSelected()){
            posisi = "Corporal";
        }
        
        String day = comboDateTambahPegawai.getSelectedItem().toString(),
                month = comboMonthTambahPegawai.getSelectedIndex() + "",
                year = comboYearTambahPegawai.getSelectedItem().toString();
        
        int d = Integer.parseInt(day);
        int m = Integer.parseInt(month + 1);
        int y = Integer.parseInt(year);
        
        Date date = Date.valueOf(LocalDate.of(y, m, d));
        
        int id = listPegawai.getModel().getSize() + 1;
        Pegawai pegawai = new Pegawai();
        
        pegawai.setId(id);
        pegawai.setName(nama);
        pegawai.setPosisi(posisi);
        pegawai.setTglLahir(date);
        
        DefaultListModel<Pegawai> model = (DefaultListModel<Pegawai>) listPegawai.getModel();
        model.addElement(pegawai);
        
        dialogTambahPegawai.setVisible(false);
        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
    }//GEN-LAST:event_btnSaveTambahPegawaiActionPerformed

